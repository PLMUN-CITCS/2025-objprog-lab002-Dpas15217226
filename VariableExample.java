
public class VariableExample {
  public static void main(String[] args) {
    // Defining the variables
    int studentAge = 15;              // Age of the student
    double itemPrice = 29.99;         // Price of an item
    String studentName = "Alice";     // Name of the student
    int counter = 10;                 // Counter variable
    int counterDifferentCase = 20;    // Counter variable with different case

    // Printing the values
    System.out.println("Student Age: " + studentAge);
    System.out.println("Item Price: $" + itemPrice);
    System.out.println("Student Name: " + studentName);
    System.out.println("Counter: " + counter);
    System.out.println("Counter (Different Case): " + counterDifferentCase);
  }
}
